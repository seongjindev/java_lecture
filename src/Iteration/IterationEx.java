package Iteration;

public class IterationEx {
    public static void main(String[] args){
        int a = 0;
        int b = 10;
        for(int i = 0;i<10;i++){
            System.out.println(i);
        }
        while(a<10){
            System.out.println(a);
            a++;
        }
        do{
            System.out.println(b);
            b++;
        }while(b<10);

    }
}
